*Purpose: Calculate taxes using Gross Income and store each seperately

CALCTAXES
 ORG $2000 ; PROGRAM STARTS AT ADDRESS $2000
 MOVEM.L D0/D6,-(SP) ; SAVE REGISTER D0
 MULU.W #25,D2     ; MULTIPLY D1 WITH 25 AND STORE IN D2 - SYNTAX
 DIVU.W #100,D2    ; DIVIDE D2 BY 100 TO GET 25% OF D1
 MULU.W #10,D3     ; MULTIPLY D1 WITH 10 AND STORE IN D3 - SYNTAX
 DIVU.W #100,D2    ; DIVIDE D3 BY 100 TO GET 10% OF D1
 MULU.W #1,D4      ; MULTIPLY D1 WITH 1 AND STORE IN D4 - SYNTAX
 DIVU.W #100,D4    ; DIVIDE D4 BY 100 TO GET 1% OF D1

ADDTAXES
 CLR.L D0           ; CLEAR REGISTER D0 TO STORE SUM OF ALL TAXES
 ADD.L D2,D0        ; ADD D2, D0
 ADD.L D3,D0        ; ADD D3, D0
 ADD.L D4,D0        ; ADD D4, D0
 MOVE.L D0,D5       ; MOVE SUM OF TAXES TO D5

CALCNET
 MOVE.L D1,D6       ; MOVE D1 TO D6
 SUB.L D5,D6        ; D6=D6-D5=NET PAY
 MOVE.L D6,D5       ; D5=NET PAY
 
 MOVEM.L (SP)+,D0/D6 ; RESTORE REGISTER D0 - SYNTAX
*END PROGRAM - RETURN CONTROL TO MONITOR
 MOVE.B #228,D7
 TRAP #14
 END